'''
В файле содержится последовательность из 10000 целых положительных чисел.
Каждое число не превышает 10000.
Определите и запишите в ответе сначала количество пар элементов последовательности,
для которых произведение элементов делится без остатка на 10, затем максимальную из сумм элементов таких пар. В данной задаче под парой подразумевается два различных элемента последовательности.
Порядок элементов в паре не важен.
'''
f = open('17-37344.txt')
s = []
for line in f:
    s.append(int(line))
k = 0
ms = 0
mp = 0
for i in range(0, len(s)-1):
    for j in range(i+1, len(s)):
        if (s[i] * s[j]) % 10 == 0:
            k += 1
            ms = max(ms, s[i] + s[j])
            mp = max(mp, s[i] * s[j])
print(k, ms, mp)